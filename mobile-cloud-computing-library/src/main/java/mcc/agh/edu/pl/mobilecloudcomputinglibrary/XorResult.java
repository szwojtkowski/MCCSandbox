package mcc.agh.edu.pl.mobilecloudcomputinglibrary;

public class XorResult {

    private double zero;
    private double one;

    public XorResult(double zero, double one){
        this.zero = zero;
        this.one = one;
    }

    public double zero(){
        return zero;
    }

    public double one(){
        return one;
    }

}
